    public void todosOsEnderecosPorId(){
        
        DefaultTableModel modelo = (DefaultTableModel) tblEnderecos.getModel();
        tblEnderecos.setRowSorter(new TableRowSorter(modelo));
        
        modelo.setRowCount(0);
        ArrayList<Endereco> listaClientePorId = enderecoBanco.pesquisaTodosEnderecosPorCpf(txtCpf.getText());
        
        listaClientePorId.forEach(a -> {
            modelo.addRow(new Object[]{
                a.getEndereco(),
                a.getLogin()}
            );
        });
        txtEnderecoPrincipal.setText(listaClientePorId.get(0).getEndereco());
    }
    
    public void alteraEnderecoPrincipal(){
        enderecoBanco.alteraEnderecoPrincipal(txtEnderecoPrincipal.getText());
    }
    
    public void excluiEndereco(){
        int Linha = tblEnderecos.getSelectedRow();
        String endereco = tblEnderecos.getModel().getValueAt(Linha, 0).toString();
        enderecoBanco.excluirEndereco(endereco);
        txtEnderecoPrincipal.setText("");
    }
    
    public void abrirTelaInserirEnderecos(){
        TelaIncluirEndereco telaIncluirEndereco = new TelaIncluirEndereco();
        telaIncluirEndereco.enviaCpf(this, txtCpf.getText());
        telaIncluirEndereco.enviaId(this, txtId.getText());
        telaIncluirEndereco.setVisible(true);
        this.dispose();
    }
    
    public void abrirTelaAlterarEnderecos(){
        int Linha = tblEnderecos.getSelectedRow();
        String endereco = tblEnderecos.getModel().getValueAt(Linha, 0).toString();
        TelaAlterarEndereco telaAlterarEndereco = new TelaAlterarEndereco();
        telaAlterarEndereco.enviaEndereco(this, endereco);
        telaAlterarEndereco.enviaCpf(this, txtCpf.getText());
        telaAlterarEndereco.enviaId(this, txtId.getText());
        telaAlterarEndereco.setVisible(true);
        this.dispose();
    }
    
    public void voltarTela(){
    TelaUsuario telaUsuario = new TelaUsuario();
        telaUsuario.enviaCpf(this, txtCpf.getText());
        telaUsuario.enviaId(this, txtId.getText());
        telaUsuario.setVisible(true);
        telaUsuario.todosOsClientesPorId();
        this.dispose();
    }
