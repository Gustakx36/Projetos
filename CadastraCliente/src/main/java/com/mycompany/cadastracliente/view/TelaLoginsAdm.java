    public void todosLogins(){
        DefaultTableModel modelo = (DefaultTableModel) tblLogins.getModel();
        tblLogins.setRowSorter(new TableRowSorter(modelo));
        
        modelo.setRowCount(0);
        ArrayList<Login> listaLogins = clienteBancoLogin.pesquisaTodosLogins();
        
        listaLogins.forEach(a -> {
            modelo.addRow(new Object[]{
                a.getId(),
                a.getUsuarioLogin(),
                a.getSenhaLogin(),
                a.isAtivoLogin()}
            );
        });
    }
    
    public void alterarLogin(){
        int Linha = tblLogins.getSelectedRow();
        String login = tblLogins.getModel().getValueAt(Linha, 1).toString();
        TelaAlterarLoginAdm alterarLoginAdm = new TelaAlterarLoginAdm();
        alterarLoginAdm.enviaLoginSenha(this, login);
        alterarLoginAdm.setVisible(true);
        this.dispose();
    }
    
     public void desativarUsuarioLogin(){
        int Linha = tblLogins.getSelectedRow();
        String login = tblLogins.getModel().getValueAt(Linha, 1).toString();
        clienteBancoLogin.desativarLogin(login);
        todosLogins();   
    }
     
     public void ativarUsuarioLogin(){
        int Linha = tblLogins.getSelectedRow();
        String login = tblLogins.getModel().getValueAt(Linha, 1).toString();
        clienteBancoLogin.ativarLogin(login);
        todosLogins();
     }
    
    public void botaoVoltar(){
        TelaAdm telaAdm = new TelaAdm();
        telaAdm.setVisible(true);
        this.dispose();
    }
    
    public void cliente(){
        int Linha = tblLogins.getSelectedRow();
        String login = tblLogins.getModel().getValueAt(Linha, 1).toString();
        TelaClienteAdm telaClienteAdm = new TelaClienteAdm();
        telaClienteAdm.enviaLoginSenha(this, login);
        telaClienteAdm.setVisible(true);
        telaClienteAdm.todosClientes();
        this.dispose();
    }
    
